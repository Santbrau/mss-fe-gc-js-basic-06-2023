// 11. Crea un programa que solicite al usuario ingresar un número y determine si es divisible
// entre 3.

let numero = prompt ("Ingrese un número y determinaremos si es divisible entre 3")

if (numero % 3 == 0) {
    alert (numero + " es divisible entre 3")
}
else {
    alert (numero + " no es divisible entre 3")
}