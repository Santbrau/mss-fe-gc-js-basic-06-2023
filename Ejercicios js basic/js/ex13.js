// 13. Realiza un programa que solicite al usuario ingresar dos números y determine si son iguales
// o diferentes.

let numeroUno = prompt ("Ingrese un número")
let numeroDos = prompt ("Ingrese segundo un número")

if (numeroUno == numeroDos) {
    alert ("Los números son iguales")
} else {
    alert ("Los números no son iguales")
}